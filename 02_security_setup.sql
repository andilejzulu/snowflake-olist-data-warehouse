GRANT ALL PRIVILEGES
ON SCHEMA OLIST_DW.SOURCE_DATA
TO ROLE DATA_ENGINEER_ROLE;

GRANT ALL PRIVILEGES
ON SCHEMA OLIST_DW.ANALYTICS
TO ROLE DATA_ENGINEER_ROLE;

