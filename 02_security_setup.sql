-- Granting usage on warehouse
GRANT USAGE 
ON WAREHOUSE OLIST_WH 
TO ROLE DATA_ENGINEER_ROLE;

-- Granting usage on database
GRANT USAGE
ON DATABASE OLIST_DW
TO ROLE DATA_ENGINEER_ROLE;

